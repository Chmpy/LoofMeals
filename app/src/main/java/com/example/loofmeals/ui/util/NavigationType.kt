package com.example.loofmeals.ui.util

/**
 * Enum class representing different types of navigation patterns used within the application.
 * This enumeration is utilized for configuring the navigation behavior in the user interface.
 *
 * @property VAR_NAVIGATION_DRAWER Represents phone navigation drawer.
 * @property NAVIGATION_RAIL Represents navigation rail.
 * @property PERMANENT_NAVIGATION_DRAWER Represents permanent navigation drawer.
 */
enum class NavigationType {
    VAR_NAVIGATION_DRAWER,
    NAVIGATION_RAIL,
    PERMANENT_NAVIGATION_DRAWER
}
